$string['magtest_help'] = '
Le module "Test de magazine" propose un test qui se base sur un ensemble de
catégories de réponses, comme les tests de psychologie qu l\'on trouve dans les
magazines. Le test permet d\'établir les catégories de classement du résultat,
et propose de définir un jeu de questions à poser aux participants. A chaque
question doit correspondre un certain nombre de propositions (une par
catégorie) permettant au participant de faire son choix.

