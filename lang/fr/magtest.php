réponse appropriée ce qui rapportera un certain nombre de points à la catégorie
correspondante.

A la fin du test, la catégorie gagnante est celle qui a collecté de meilleur
score. Une conclusion est alors montrée aux utilisateurs, laquelle souligne la
conclusion "gagnante", suivie éventuellement par une conclusion plus générale.

Les groupes de participants issus du test et classés par catégorie gagnante
peuvent être convertis en groupes Moodle du cours courant. Ce module peut donc
servir comme "discriminateur à critère" pour former des groupes par l\'action
des participants eux-mêmes.';

